/**
 * Global `.env` generator (SHARED_SPEC §1.6 + §5.1).
 *
 * Produces an `envpact-global.env` file containing every shared
 * secret in the vault. Walks `vault.shared.*` alphabetically and
 * emits `KEY=value` lines (quoted per §5 envwriter rules); encrypted
 * (`enc:*`) entries become `# KEY: encrypted — decrypt-via-cli`
 * comment lines.
 *
 * The dashboard cannot read `~/.envpact/.env.example.global` (no
 * filesystem access in browsers), so it falls back to the
 * "alphabetical, no comments" auto-template behaviour described in
 * §1.6 for the no-template case. The user can then save the
 * download to `~/.envpact/.env` or run envpact-cli `--sync-global`
 * to keep things in sync.
 *
 * Quoting rule (§5): a value is double-quoted when it contains
 * whitespace, `\n`, `\r`, `=`, `#`, or starts with whitespace, OR
 * when it is empty. Backslashes and double-quotes are JSON-escaped
 * inside the quoted form.
 */

import { entryValue } from './resolver.js';

const ENC_PREFIX = 'enc:';

function needsQuoting(v) {
  if (v === '') return true;
  if (/^\s/.test(v)) return true;
  return /[\s#"'\\=\n\r]/.test(v);
}

function quote(v) {
  return `"${v.replace(/\\/g, '\\\\').replace(/"/g, '\\"').replace(/\n/g, '\\n').replace(/\r/g, '\\r')}"`;
}

/**
 * Format a single value per §5 envwriter rules.
 * Exported for tests; callers normally use `renderGlobalEnv`.
 */
export function formatEnvValue(v) {
  return needsQuoting(v) ? quote(v) : v;
}

/**
 * Render the global `.env` body as a string.
 *
 * @param {object} vault - The full v3 vault (`{shared, projects, …}`).
 * @param {object} [opts]
 * @param {string} [opts.now] - ISO-8601 timestamp for the header
 *   (defaults to `new Date().toISOString()`). Tests pin this.
 * @returns {string} The full file content (header + body, ends in '\n').
 */
export function renderGlobalEnv(vault, opts = {}) {
  const now = typeof opts.now === 'string' ? opts.now : new Date().toISOString();
  const lines = [
    `# Generated by envpact (global) on ${now}`,
    '# DO NOT COMMIT — managed by envpact',
    '',
  ];
  const shared = (vault && typeof vault === 'object' && vault.shared) || {};
  const keys = Object.keys(shared).sort();
  for (const key of keys) {
    const v = entryValue(shared[key]);
    if (v === null) {
      lines.push(`# ${key}: invalid entry`);
      continue;
    }
    if (v.startsWith(ENC_PREFIX)) {
      lines.push(`# ${key}: encrypted — decrypt-via-cli`);
      continue;
    }
    lines.push(`${key}=${formatEnvValue(v)}`);
  }
  return lines.join('\n') + '\n';
}

/**
 * Trigger a browser download of the global `.env`.
 * Filename is `envpact-global.env` — browsers can't write to a
 * user-chosen path securely; this just hands the file to the user
 * to save under `~/.envpact/.env` themselves (or let
 * envpact-cli `--sync-global` regenerate it locally).
 */
export function downloadGlobalEnv(vault, filename = 'envpact-global.env') {
  const content = renderGlobalEnv(vault);
  const blob = new Blob([content], { type: 'text/plain' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  setTimeout(() => { URL.revokeObjectURL(url); a.remove(); }, 100);
}
